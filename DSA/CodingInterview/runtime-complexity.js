// Runtime Complexity
// 1. Time Complexity
// 2. Space Complexity
// 3. Auxiliary Space Complexity 

// 1. Time Complexity
// O(1) - Constant Time: The execution time remains constant regardless of input size.
// O(logn) - Logarithmic Time: Execution time increases logarithmically with input size, often seen in divide-and-conquer algorithms.
// O(n) - Linear Time: Execution time grows linearly with input size, common in simple iterations.
// O(n^2) - Quadratic Time: Execution time grows quadratically, often seen in nested loops.
// O(n^3) - Cubic Time: Execution time grows cubically, typically involving triple nested loops.
// O(2^n) - Exponential Time: Execution time doubles with each additional input, common in recursive algorithms.
// O(n!) - Factorial Time: Execution time grows factorially, seen in algorithms like generating all permutations.

// 2. Space Complexity
// O(1) - Constant Space: Memory usage remains constant regardless of input size.
// O(logn) - Logarithmic Space: Memory usage grows logarithmically with input size.
// O(n) - Linear Space: Memory usage grows linearly with input size.
// O(n^2) - Quadratic Space: Memory usage grows quadratically with input size.
// O(n^3) - Cubic Space: Memory usage grows cubically with input size.
// O(2^n) - Exponential Space: Memory usage doubles with each additional input.
// O(n!) - Factorial Space: Memory usage grows factorially with input size.

// 3. Auxiliary Space Complexity
// Auxiliary space is the extra space used by an algorithm, excluding the input space.
// O(1) - Constant Auxiliary Space: No extra space is used, or a fixed amount regardless of input size.
// O(logn) - Logarithmic Auxiliary Space: Extra space grows logarithmically with input size.
// O(n) - Linear Auxiliary Space: Extra space grows linearly with input size.
// O(n^2) - Quadratic Auxiliary Space: Extra space grows quadratically with input size.
// O(n^3) - Cubic Auxiliary Space: Extra space grows cubically with input size.
// O(2^n) - Exponential Auxiliary Space: Extra space doubles with each additional input.
// O(n!) - Factorial Auxiliary Space: Extra space grows factorially with input size.
